#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	unsigned char	*res;
	size_t			i;

	res = (unsigned char *)s;
	i = 0;
	while (i < n)
	{
		if (res[i] == (unsigned char)c)
			return (&res[i]);
		++i;
	}
	return (0);
}
